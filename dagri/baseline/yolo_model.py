import os
import json
import shutil
import yaml
import tempfile
from pathlib import Path

from ultralytics import YOLO
from pycocotools.coco import COCO
from pycocotools.cocoeval import COCOeval

from dagri.interfaces import BaselineConfig, DatasetProperties, EvaluationResults, PredictionResult, BoundingBox
from dagri.baseline.utils import create_coco_gt_json_from_yolo


class YoloUltralyticsModel:
    """
    A wrapper class for the YOLO model using the Ultralytics library. This class will handle training, evaluation, and inference using the YOLO model.
    """
    def __init__(self, baseline_config: BaselineConfig):
        # Initialize config
        self.train_config = baseline_config.training_config
        self.evaluation_config = baseline_config.evaluation_config
        # Initialize model
        self.model = YOLO(baseline_config.pretrained_weights_path)
    
    def custom_train(self, dataset_properties: DatasetProperties, output_dir_for_best_weights: str) -> str:
        """
        Train YOLO model and save best weights to output directory.
        
        Args:
            dataset_properties: Properties of the dataset to use for training
            output_dir_for_best_weights: Directory to save best.pt

        Returns:
            Path to the copied best.pt file.
        """
        os.makedirs(output_dir_for_best_weights, exist_ok=True)
        
        # Create data.yaml from dataset properties
        data_path = self._create_data_yaml(dataset_properties, output_dir_for_best_weights)
        
        output_dir_for_best_weights = os.path.abspath(output_dir_for_best_weights)

        train_kwargs = {
            "data": data_path,
            "epochs": self.train_config.epochs,
            "batch": self.train_config.batch_size,
            "imgsz": self.train_config.input_size,
            "lr0": self.train_config.learning_rate,
            "seed": self.train_config.seed,
            "patience": self.train_config.early_stopping_patience,
            "deterministic": True,
            "save": True,
            "save_period": -1,
            "plots": False,
            "verbose": False,
            "project": output_dir_for_best_weights,
            "name": "train",
            "exist_ok": True,
        }

        # Ultralytics expects `augment` to be bool. Pass configured augmentation values
        # as individual training arguments (hsv_h, degrees, fliplr, etc.).
        aug_cfg = self.train_config.traditional_augmentation_config or {}
        if isinstance(aug_cfg, dict):
            train_kwargs.update(aug_cfg)
       
        
        self.model.train(**train_kwargs)

        trainer_best = getattr(getattr(self.model, "trainer", None), "best", None)
        weight_src = str(trainer_best) if trainer_best else ""
        if not weight_src or not os.path.exists(weight_src):
            weight_src = os.path.join(output_dir_for_best_weights, "train", "weights", "best.pt")
        weight_dst = os.path.join(output_dir_for_best_weights, "best.pt")
        if not os.path.exists(weight_src):
            raise FileNotFoundError(f"Best weight not found after training: {weight_src}")

        shutil.copy(weight_src, weight_dst)
        return weight_dst
    
    def custom_evaluate_on_test_set(self, best_weight_path: str, dataset: DatasetProperties) -> EvaluationResults:
        """
        Evaluate the trained model on test set and return typed COCO metrics.
        
        Args:
            best_weight_path: Path to best model weights generated by custom_train
        """
        if not os.path.exists(best_weight_path):
            raise FileNotFoundError(f"Best weight file not found: {best_weight_path}")

        dataset_properties = dataset
        test_images_dir = dataset_properties.test_images_dir
        test_labels_dir = dataset_properties.test_labels_dir

        if not test_images_dir or not test_labels_dir:
            raise ValueError("dataset_properties must include test_images_dir and test_labels_dir")

        with tempfile.TemporaryDirectory(prefix="eval_test_") as tmp_dir:
            data_path = self._create_data_yaml(dataset_properties, tmp_dir)
            coco_gt_json = os.path.join(tmp_dir, "test_coco_gt.json")

            create_coco_gt_json_from_yolo(
                images_dir=test_images_dir,
                output_json_path=coco_gt_json,
                labels_dir=test_labels_dir,
                class_names=dataset_properties.class_names,
            )

            eval_model = YOLO(best_weight_path)

            val_kwargs = {
                "data": data_path,
                "split": "test",
                "save_json": True,
                "project": tmp_dir,
                "name": "evaluation",
                "exist_ok": True,
                "plots": False,
                "verbose": False,
            }

            if self.evaluation_config.image_size is not None:
                val_kwargs["imgsz"] = self.evaluation_config.image_size
            if self.evaluation_config.confidence_threshold is not None:
                val_kwargs["conf"] = self.evaluation_config.confidence_threshold
            if self.evaluation_config.iou_threshold is not None:
                val_kwargs["iou"] = self.evaluation_config.iou_threshold
            if self.evaluation_config.max_detections is not None:
                val_kwargs["max_det"] = self.evaluation_config.max_detections

            val_results = eval_model.val(**val_kwargs)

            candidate_dirs = [os.path.join(tmp_dir, "evaluation")]
            for maybe_dir in [
                getattr(val_results, "save_dir", None),
                getattr(getattr(eval_model, "validator", None), "save_dir", None),
                getattr(getattr(eval_model, "trainer", None), "save_dir", None),
            ]:
                if maybe_dir:
                    candidate_dirs.append(str(maybe_dir))

            pred_json_path = ""
            for candidate_dir in candidate_dirs:
                candidate_json = os.path.join(candidate_dir, "predictions.json")
                if os.path.exists(candidate_json):
                    pred_json_path = candidate_json
                    break

            if not os.path.exists(pred_json_path):
                raise FileNotFoundError(
                    "Predictions JSON not  0.04created. Checked: " + ", ".join(candidate_dirs)
                )

            coco_gt = COCO(coco_gt_json)
            coco_dt = coco_gt.loadRes(pred_json_path)
            coco_eval = COCOeval(coco_gt, coco_dt, iouType="bbox")
            coco_eval.evaluate()
            coco_eval.accumulate()
            coco_eval.summarize()

            stats = coco_eval.stats

            return EvaluationResults(
                COCO_AP=float(stats[0]),
                COCO_AP50=float(stats[1]),
                COCO_AP75=float(stats[2]),
                AP_small=float(stats[3]),
                AP_medium=float(stats[4]),
                AP_large=float(stats[5]),
            )

    def custom_evaluate(self, best_weight_path: str, dataset: DatasetProperties) -> EvaluationResults:
        """Backward-compatible alias for test-set evaluation."""
        return self.custom_evaluate_on_test_set(best_weight_path, dataset)

    def _create_data_yaml(self, dataset_properties: DatasetProperties, output_dir: str) -> str:
        """
        Create a YOLO data.yaml from dataset properties.
        
        Args:
            output_dir: Directory to save the generated data.yaml
            
        Returns:
            Path to the generated data.yaml file
        """
        dataset_properties = dataset_properties
        # Get properties
        root_dir = dataset_properties.root_dir
        class_names = dataset_properties.class_names or ["object"]
        
        # Create data.yaml content using paths from dataset properties
        data_yaml = {
            "path": root_dir,
            "train": dataset_properties.train_images_dir,
            "val": dataset_properties.val_images_dir,
            "test": dataset_properties.test_images_dir,
            "nc": dataset_properties.num_classes or len(class_names),
            "names": {i: name for i, name in enumerate(class_names)}
        }
        
        # Write to yaml file
        yaml_path = os.path.join(output_dir, "data.yaml")
        os.makedirs(output_dir, exist_ok=True)
        with open(yaml_path, "w") as f:
            yaml.dump(data_yaml, f, default_flow_style=False)
        
        return yaml_path
    
    def custom_predict(self, model_weight: str, image_dir: str, conf: float, iou: float, max_det: int) -> list[PredictionResult]:
        """
        Run inference on a directory of images and return prediction results.
        
        Args:
            model_weight: Path to the pre-trained model weights
            image_dir: Path to directory containing input images
            conf: Confidence threshold for predictions
            iou: IoU threshold for non-maximum suppression
            max_det: Maximum number of detections per image
            
        Returns:
            List of PredictionResult objects, one for each image
        """
        model = YOLO(model_weight)
        predict_result = model.predict(source=image_dir, save=False, conf=conf, iou=iou, max_det=max_det)
        
        prediction_results = []
        
        for result in predict_result:
            # Get normalized boxes (0-1), classes, and confidences
            boxes_normalized = result.boxes.xywhn.cpu().numpy()  # [N, 4]
            classes = result.boxes.cls.cpu().numpy()              # [N]
            confidences = result.boxes.conf.cpu().numpy()         # [N]
            
            # Create BoundingBox objects
            predicted_boxes = [
                BoundingBox(x_center=float(box[0]), y_center=float(box[1]), width=float(box[2]), height=float(box[3]))
                for box in boxes_normalized
            ]
            
            # Create PredictionResult
            pred_result = PredictionResult(
                image_path=result.path,
                classes=classes.tolist(),
                confidences=confidences.tolist(),
                predicted_boxes=predicted_boxes
            )
            prediction_results.append(pred_result)
        
        return prediction_results
        

    def get_optimal_conf_threshold_for_scoring(
        self,
        dataset_properties: DatasetProperties,
        model_weight: str,
        conf_min: float = 0.001,
        conf_max: float = 0.9,
        num_points: int = 20,
    ) -> float:
        """
        Grid-search confidence thresholds on the validation split and return the best threshold.

        Selection criterion: maximize validation mAP50.
        """
        if not os.path.exists(model_weight):
            raise FileNotFoundError(f"Model weight file not found: {model_weight}")
        if not dataset_properties.val_images_dir or not dataset_properties.val_labels_dir:
            raise ValueError("dataset_properties must include val_images_dir and val_labels_dir")
        if num_points < 2:
            raise ValueError("num_points must be >= 2")
        if not (0.0 <= conf_min < conf_max <= 1.0):
            raise ValueError("conf_min/conf_max must satisfy 0.0 <= conf_min < conf_max <= 1.0")

        model = YOLO(model_weight)

        thresholds = [
            conf_min + (conf_max - conf_min) * (i / (num_points - 1))
            for i in range(num_points)
        ]

        best_conf = thresholds[0]
        best_score = -1.0

        with tempfile.TemporaryDirectory(prefix="conf_threshold_search_") as tmp_dir:
            data_path = self._create_data_yaml(dataset_properties, tmp_dir)

            for conf in thresholds:
                val_kwargs = {
                    "data": data_path,
                    "split": "val",
                    "conf": float(conf),
                    "save_json": False,
                    "plots": False,
                    "verbose": False,
                }

                if self.evaluation_config.image_size is not None:
                    val_kwargs["imgsz"] = self.evaluation_config.image_size
                if self.evaluation_config.iou_threshold is not None:
                    val_kwargs["iou"] = self.evaluation_config.iou_threshold
                if self.evaluation_config.max_detections is not None:
                    val_kwargs["max_det"] = self.evaluation_config.max_detections

                val_result = model.val(**val_kwargs)
                map50 = self._extract_map50(val_result)

                if map50 > best_score:
                    best_score = map50
                    best_conf = conf

        return float(round(best_conf, 6))

    @staticmethod
    def _extract_map50(val_result) -> float:
        """
        Robustly extract mAP50 from Ultralytics validation results.
        """
        box_metrics = getattr(val_result, "box", None)
        if box_metrics is not None and hasattr(box_metrics, "map50"):
            return float(box_metrics.map50)

        results_dict = getattr(val_result, "results_dict", None) or {}
        for key in ("metrics/mAP50(B)", "metrics/mAP50", "map50"):
            if key in results_dict:
                return float(results_dict[key])

        raise RuntimeError("Could not extract mAP50 from validation results")
